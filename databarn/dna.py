_type = type  # Alias to avoid confusion with the 'type' attribute in BaseGrain

# @beartype
class BaseDna:
    """Internal metadata/behavior container shared by Cob models and instances.

    The class-level view stores model metadata (grains, labels, constraints).
    The instance-level view stores per-cob runtime state (grains, barn links,
    parent links, mapping-like helpers).
    """

    @staticmethod
    def _type_display_name(type_hint: Any) -> str:
        """Return a user-facing type name for error messages."""
        if isinstance(type_hint, _type):
            return type_hint.__name__
        forward_arg = getattr(type_hint, "__forward_arg__", None)
        if isinstance(forward_arg, str):
            return forward_arg
        if isinstance(type_hint, str):
            return type_hint
        return str(type_hint)

    @staticmethod
    def _barn_model_matches(expected_model_type: Any, actual_model_type: _type[Any]) -> bool:
        """Compare Barn model type args while tolerating unresolved forward references."""
        if isinstance(expected_model_type, _type):
            return actual_model_type is expected_model_type
        expected_name = BaseDna._type_display_name(expected_model_type)
        expected_short_name = expected_name.split(".")[-1]
        return actual_model_type.__name__ == expected_short_name

    @staticmethod
    def _resolve_type_hint(type_hint: Any, model: _type[Cob]) -> Any:
        """Resolve string annotations against the model module when possible."""
        if not isinstance(type_hint, str):
            return type_hint
        module = sys.modules.get(model.__module__)
        globalns = dict(vars(module)) if module is not None else {}
        globalns[model.__name__] = model
        globalns.setdefault("__builtins__", __builtins__)
        try:
            return eval(type_hint, globalns, globalns)
        except Exception:
            return type_hint

    # Model
    model: _type[Cob]
    label_grain_map: dict[str, _type[BaseGrain] |
                          BaseGrain]  # {label: grain or grainob}
    # This is an ordered set of all Cob instances of this model.
    # Used for consistency cascading updates.
    cobs: list[Cob]
    blueprint: Literal[*BLUEPRINTS]
    on_extra_kwargs: str
    # This is set by relationship decorators to link back to the
    # synthetic grain they generate for the outer model.
    _outer_model_grain: _type[BaseGrain] | None = None

    # Cob instance
    cob: Cob  # type: ignore
    autoid: int  # If the primakey is not provided, autoid will be used as primakey
    barns: list[Barn]
    _container_parent_map: dict[BaseGrain | Barn, Cob]  # Map of grain objects to their parent cobs

    @classmethod
    # Set by decorators
    def _set_outer_model_grain(klass, outer_model_grain: _type[BaseGrain]) -> None:
        """Store the synthetic grain generated by relationship decorators."""
        klass._outer_model_grain = outer_model_grain

    @classmethod_only
    def _embed_grain(klass, label: str, grain: _type[BaseGrain]) -> None:
        """Register a grain under `label` in the Cob-model or Cob instance.

        Args:
            grain: type[BaseGrain] or BaseGrain object to register.
            label: Attribute name for the grain.
        """
        if label in klass.labels:
            raise CobConsistencyError(fo(f"""
                The Grain '{label}' has already been
                set up in this {klass}."""))
        grain._validate()
        klass.label_grain_map[label] = grain  # type: ignore

    @classmethod_only
    def create_barn(klass) -> Barn:  # type: ignore
        """Create a new Barn for the model.

        Returns:
            A new Barn object for the model.
        """
        from .barn import Barn  # Lazy import to avoid circular imports
        return Barn(model=klass.model)

    @classmethod_only
    def create_cob_from_dict(klass,
                             dikt: dict,
                             replace_space_with: str | None = "_",
                             replace_dash_with: str | None = "__",
                             suffix_keyword_with: str | None = "_",
                             prefix_leading_num_with: str | None = "n_",
                             replace_invalid_char_with: str | None = "_",
                             suffix_existing_attr_with: str | None = "_",
                             # type: ignore
                             custom_key_converter: Callable[[Any], str] | None = None) -> Cob:
        """Create a Cob instance from a dictionary.

        Args:
            dikt: The dictionary to convert to a Cob.
            replace_space_with: Replace spaces in keys with this string.
            replace_dash_with: Replace dashes in keys with this string.
            suffix_keyword_with: Suffix keywords with this string.
            prefix_leading_num_with: Prefix leading numbers in keys with this string.
            replace_invalid_char_with: Replace invalid characters in keys with this string.
            suffix_existing_attr_with: Suffix existing attributes with this string.
            custom_key_converter: A custom function to convert keys.
        Returns:
            A new Cob instance bound to this model.
        """
        from .funcs import dict_to_cob  # Lazy import to avoid circular imports
        cob = dict_to_cob(
            dikt,
            model=klass.model,
            replace_space_with=replace_space_with,
            replace_dash_with=replace_dash_with,
            suffix_keyword_with=suffix_keyword_with,
            prefix_leading_num_with=prefix_leading_num_with,
            replace_invalid_char_with=replace_invalid_char_with,
            suffix_existing_attr_with=suffix_existing_attr_with,
            custom_key_converter=custom_key_converter,)
        return cob

