    @dual_property
    def grains(owner) -> tuple[type[BaseGrain], ...]:
        """Return a tuple of the grains of the model or cob."""
        return tuple(owner.label_grain_map.values())

    @dual_property
    def labels(owner) -> tuple[str, ...]:
        """Return a tuple of the labels of the model or cob."""
        return tuple(owner.label_grain_map.keys())

    @dual_property
    def primakey_labels(owner) -> tuple[str, ...]:
        """Return a tuple of the primakey labels of the model or cob."""
        labels = [grain.label for grain in owner.grains if grain.pk]
        return tuple(labels)

    @dual_property
    def primakey_defined(owner) -> bool:
        """Return True if the primakey is defined for the model or cob."""
        return (len(owner.primakey_labels) > 0)

    @dual_property
    def is_compos_primakey(owner) -> bool:
        """Return whether the model uses a composite primary key."""
        return (len(owner.primakey_labels) > 1)

    @dual_property
    def primakey_len(owner) -> int:
        """Return number of primary key grains, or ``1`` when none are defined."""
        return (len(owner.primakey_labels) or 1)

    @dual_method
    def get_grain(owner, label: str, default: Any = MISSING_ARG) -> _type[BaseGrain] | Any:
        """Return the model grain registered under ``label``.

        Args:
            label: The Grain label.
            default: Value to return when the label does not exist.

        Returns:
            The matching Grain, or ``default`` when provided and missing.
        """
        if default is MISSING_ARG and label not in owner.labels:
            raise KeyError(fo(f"""
                The Grain '{label}' does not exist in the model '{owner.model.__name__}'."""))
        return owner.label_grain_map.get(label, default)

    @property
    def active_grains(self) -> tuple[BaseGrain, ...]:
        """Return a tuple of Cob's grains whose values have been set and not been deleted."""
        grains = [grain for grain in self.grains if grain.attr_exists()]
        return tuple(grains)

    @property
    def primakey_grains(self) -> tuple[BaseGrain, ...]:
        """Return a tuple of the Cob's primakey grains."""
        return tuple(self.get_grain(label) for label in self.primakey_labels)

    @property
    def parents(self) -> tuple[Cob, ...]:
        """Return a tuple of parent cobs that currently contain this cob."""
        return tuple(self._container_parent_map.values())

    @property
    def latest_parent(self) -> Cob | None:  # type: ignore
        """Return the latest parent cob if exists, otherwise None.

        CAUTION: If the cob has multiple parents, only the last one is returned.
        """
        if not self.parents:
            return None
        return self.parents[-1]

    # Cob object methods
    def __init__(self, cob: Cob) -> None:  # type: ignore
        """Initialize runtime DNA state for a concrete Cob instance."""
        self.cob = cob
        self.autoid = id(cob)  # Default autoid is the id of the cob object
        # Register this cob in the model's catalog
        self.cobs.append(cob)
        self.barns = []
        self._container_parent_map = {}
        self.label_grain_map = {}  # Instance-level grainobs
        # Use the class-level grains here
        for grain in self.__class__.grains:
            grainob = grain(cob)
            # Use the instance-level embedding method
            # to register the grainob in the cob's dna
            self._embed_grainob(grain.label, grainob)

    def _add_barn(self, barn: Barn) -> None:  # type: ignore
        """Register a Barn that currently contains this Cob."""
        self.barns.append(barn)

    def _remove_barn(self, barn: Barn) -> None:  # type: ignore
        """Unregister a Barn that no longer contains this Cob."""
        self.barns.remove(barn)

    def _embed_grainob(self, label: str, grainob: BaseGrain) -> None:
        """Embed a Grain object in the Cob instance under `label`."""
        if label in self.label_grain_map:
            raise CobConsistencyError(fo(f"""
                The Grain '{label}' has already been
                set up in this Cob instance."""))
        self.label_grain_map[label] = grainob

    def dyn_add_grain(self, label: str, type: Any = Any) -> BaseGrain:
        """Add a grain to the Cob instance at runtime.

        Args:
            label: The grain label.
            type: The grain type.

        Returns:
            The added grain instance.
        """
        if self.blueprint != DYNAMIC:
            raise SchemaViolationError(fo(f"""
                Cannot insert Grain '{label}', because this Cob
                '{self.model.__name__}' has been defined by
                blueprint '{self.blueprint}'."""))
        grain = create_grain_class()
        grain.__setup__(parent_model=self.model, label=label, type=type)
        grainob = grain(self.cob)
        self._embed_grainob(label, grainob)
        return grainob

    def _dyn_remove_grain(self, label: str) -> None:
        """Remove a grain from the Cob instance at runtime.

        Args:
            label: The grain label to remove.
        """
        if self.blueprint != DYNAMIC:
            raise SchemaViolationError(fo(f"""
                Cannot remove the Grain '{label}' because this Cob
                '{self.model.__name__}' has been defined by
                blueprint '{self.blueprint}'."""))
        if label not in self.label_grain_map:
            raise KeyError(fo(f"""
                Cannot remove the Grain '{label}', because it
                does not exist in the cob."""))
        del self.label_grain_map[label]

    def get_keyring(self) -> Any | tuple[Any, ...] | Sentinel:
        """Return the key used by Barn lookups for this Cob.

        If no primary key is defined, ``autoid`` is used.

        Returns:
            Primary key value, composite key tuple, or `ABSENT` when a declared
            primary key grain is currently unset.
        """
        if not self.primakey_defined:
            return self.autoid
        primakeys = []
        for grain in self.primakey_grains:
            if not grain.attr_exists():
                return ABSENT
            primakeys.append(grain.get_value())
        if not self.is_compos_primakey:
            return primakeys[0]
        return tuple(primakeys)

    def _verify_constraints(self, grain: BaseGrain, value: Any) -> None:
        """Validate type and constraint rules before assigning ``value`` to ``grain``.

        Args:
            grain: Target grain.
            value: Candidate value to assign.
        """
        resolved_type = self._resolve_type_hint(grain.type, self.model)
        if resolved_type is not Any:
            bearable = False
            try:
                bearable = is_bearable(value, resolved_type)
            except Exception as exc:
                # Postponed/quoted forward references can fail to resolve in beartype.
                # Keep DataBarn's exception surface stable for callers.
                from .barn import Barn  # Lazy import to avoid circular imports
                if isinstance(value, Barn):
                    expected_model_type = None
                    origin_type = get_origin(resolved_type)
                    if origin_type is Barn:
                        type_args = get_args(resolved_type)
                        if type_args:
                            expected_model_type = type_args[0]
                    else:
                        type_name = self._type_display_name(resolved_type)
                        if type_name.startswith("Barn[") and type_name.endswith("]"):
                            expected_model_name = type_name[5:-
                                                            1].strip().strip("'\"")
                            if value.model.__name__ == expected_model_name:
                                bearable = True
                    if expected_model_type is not None and self._barn_model_matches(expected_model_type, value.model):
                        bearable = True
                if not bearable:
                    raise GrainTypeMismatchError(fo(f"""
                        Cannot assign '{grain.label}={value}' because the Grain
                        type '{resolved_type}' could not be resolved ({exc.__class__.__name__}).""")) from exc
            if not bearable:
                raise GrainTypeMismatchError(fo(f"""
                    Cannot assign '{grain.label}={value}' because the Grain
                    was defined as {resolved_type}, but got {type(value)}."""))
            from .barn import Barn  # Lazy import to avoid circular imports
            origin_type = get_origin(resolved_type)
            if origin_type is Barn:
                type_args = get_args(resolved_type)
                if type_args:
                    expected_model_type = type_args[0]
                    if not self._barn_model_matches(expected_model_type, value.model):
                        expected_model_name = self._type_display_name(
                            expected_model_type)
                        raise GrainTypeMismatchError(fo(f"""
                            Cannot assign '{grain.label}={value}' because the Grain
                            was defined as 'Barn[{expected_model_name}]',
                            but got 'Barn[{value.model.__name__}]'."""))
        if grain.frozen and grain.attr_exists():
            raise CobConstraintViolationError(fo(f"""
                Cannot assign '{grain.label}={value}' because the Grain
                was defined as 'frozen=True'."""))
        if grain.pk and self.barns:
            raise CobConstraintViolationError(fo(f"""
                Cannot assign '{grain.label}={value}' because the Grain
                was defined as 'pk=True' and the Cob has been added to a barn."""))
        if grain.unique and self.barns:
            for barn in self.barns:
                barn._check_uniqueness_by_value(grain, value)

    def _add_parent(self, container: BaseGrain | Barn, parent: Cob) -> None:
        """Register a parent Cob reference."""
        self._container_parent_map[container] = parent

    def _remove_parent(self, container: BaseGrain | Barn) -> None:
        """Remove a parent Cob reference."""
        del self._container_parent_map[container]

    def _set_parent_for_new_value_if(self, grainob: BaseGrain):
        """Attach this cob as parent when assigning child Cob/Barn values."""
        # Lazy import to avoid circular imports
        from .barn import Barn
        from .cob import Cob
        value = grainob.get_value()
        if isinstance(value, Barn):
            child_barn = value  # Just for clarity
            child_barn._add_parent_cob(self.cob)
        elif isinstance(value, Cob):
            child_cob = value  # Just for clarity
            child_cob.__dna__._add_parent(grainob, self.cob)

    def _remove_parent_if(self, grain: BaseGrain) -> None:
        """Detach parent links for previous child Cob/Barn values when replaced."""
        # Lazy import to avoid circular imports
        from .barn import Barn
        from .cob import Cob
        # First assignment has no previous value to detach.
        if not grain.attr_exists():
            return
        value = grain.get_value()
        if isinstance(value, Barn):
            child_barn = value  # Just for clarity
            # Remove the parent for the barn
            child_barn._remove_parent_cob(self.cob)
        elif isinstance(value, Cob):
            child_cob = value  # Just for clarity
            child_cob.__dna__._remove_parent(grain)

    def _check_and_get_comparables(self, cob: Cob, strict: bool = True) -> list[BaseGrain]:
        """Validate comparison compatibility and return comparable grains."""
        if not isinstance(cob, self.model):
            if strict:
                raise CobConsistencyError(fo(f"""
                    Cannot compare this Cob '{self.model.__name__}' with
                    '{type(cob).__name__}', because they are different types."""))
            return []
        comparables = [grain for grain in self.grains if grain.comparable]
        if not comparables and strict:
            raise CobConstraintViolationError(fo(f"""
                Cannot compare Cob '{self.model.__name__}' with '{type(cob).__name__}'
                because they have no comparable grains in common. To enable comparison,
                set comparable=True on at least one grain in the Cob-model."""))
        return comparables

    # dict-like methods
    def items(self) -> Iterator[tuple[str, Any]]:
        """Yield ``(label, value)`` for active grains."""
        for grain in self.active_grains:
            yield grain.label, grain.get_value()

    def keys(self) -> Iterator[str]:
        """Yield labels of active grains."""
        for grain in self.active_grains:
            yield grain.label

    def clear(self) -> None:
        """Remove all values from the cob."""
        for grain in self.active_grains:
            del self.cob[grain.label]

